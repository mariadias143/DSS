    public void mostraStock() throws SQLException{
        DefaultTableModel model = new DefaultTableModel();
        DefaultTableModel model2 = new DefaultTableModel();
        Map<String,Integer> stock = this.configurafacil.verificarStock();
        Map<Integer,String> novo =  this.configurafacil.encomendasFaziveis();
        String [] cabecalho ={"Componente","Stock"};
        String [] cabecalho2 = {"Encomenda","Cliente"};
        model.setColumnIdentifiers(cabecalho);
        model2.setColumnIdentifiers(cabecalho2);
        stock.entrySet().stream().forEach(entry-> model.addRow(new String []{entry.getKey(),String.valueOf(entry.getValue())}));
        jTable1.setModel(model);
        novo.entrySet().stream().forEach(entry-> model2.addRow(new String []{String.valueOf(entry.getKey()),entry.getValue()}));
        jTable2.setModel(model2);
    }

